#we can change the value of an element in a list below by appending the position to equal a new value of the item, run it!
garden = ["beans", "peppers", "tomatoes", "onions", "basil"]
print(garden)

garden[0] = "eggplant"
print(garden)


#you can add append a new element on the list by using ".append" on the list, run it!
colors = ["red", "white", "yellow", "green", "blue"]
print(colors)

colors.append("purple")
print(colors)

#an empty list can be appended and built with ".append" and the element name.
wood = []
wood.append("beech")
wood.append("cherry")
wood.append("oak")
wood.append("pine")
print(wood)
