#in Python using whole integers is easy like a calculator and supports the order of operations, run it!
print(5 + 3)
print(5 * 6)
print(4 - 6)
print(9 * 8 - 2 + 20)
print(7 - 13 + 17 * 2)

#in Python a decimal point in an integer is called a float, dividing two integers will still equal a float, adding, subtracting, a float and integer will also return a float run it!
print(4.9 + 2.6)
print(8.7 - 9.3)
print(6/2)
print(36/6)
print(11.0 + 4)
print(9 - 5.0)

#exponents in python are represented by "**", run it!
print(9 ** 2)
print(14 ** 8)
print(6 ** 4)

#you can assign numbers to variables, run it!
a, b, c = (1, 2, 3)
print(a, b, c)

#when writing large numbers you can use underscores to make it more readable, run it!
huge_number = 278_965_311_076_111
print(huge_number)

